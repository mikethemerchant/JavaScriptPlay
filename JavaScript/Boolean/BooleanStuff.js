
// ==
console.log(1 == 1);

console.log('hello' == 'hello');

console.log('1' ==  1);

console.log(0 == false);


// ===   it is checking if the type is also the same
console.log(1 === 1);

console.log('hello' === 'hello');

console.log('1' ===  1);

console.log(0 === false);

